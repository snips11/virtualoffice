$(document).ready(function(){

		//Hide div w/id extra
	   $("#extra_form").css("display","none");

		// Add onclick handler to checkbox w/id checkme
	   $("#post").click(function(){

		// If checked
		if ($("#post").is(":checked"))
		{
			//show the hidden div
			$("#extra_form").show("slow");
		}
		else
		{
			//otherwise, hide it
			$("#extra_form").hide("slow");
		}
	  });

	});
    
